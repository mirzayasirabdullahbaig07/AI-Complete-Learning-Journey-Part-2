# ======================================================
# Central Limit Theorem (CLT) – Simplified Notes
# ======================================================

# Definition
# - CLT states: For large sample size (n > 30), 
#   the distribution of sample means becomes ~ Normal,
#   regardless of the population’s distribution (normal, skewed, etc.).
# - Mean of sampling distribution = population mean (μ)
# - Standard deviation = σ / √n  (called Standard Error)

# ------------------------------------------------------
# Formula
#   X̄ ~ N( μ , σ/√n )
#   Z = (X̄ - μ) / (σ/√n)

# ------------------------------------------------------
# Assumptions
# 1. Samples drawn randomly
# 2. Observations independent
# 3. Sample size large (n > 30)
# 4. Finite population variance

# ------------------------------------------------------
# Importance
# - Allows inference about population even if distribution is unknown
# - Basis for hypothesis testing & confidence intervals
# - Larger sample size → sample mean closer to population mean

# ------------------------------------------------------
# Example
# Population mean = 100, σ = 20, sample size n = 100
# Standard Error = σ / √n = 20 / √100 = 2
# Distribution of sample mean: N(100, 2)

# ------------------------------------------------------
# Steps to Solve CLT Problems
# 1. Identify mean (μ), std dev (σ), sample size (n)
# 2. Compute standard error = σ / √n
# 3. Find Z-score = (X̄ - μ) / (σ/√n)
# 4. Use Z-table to find probability
