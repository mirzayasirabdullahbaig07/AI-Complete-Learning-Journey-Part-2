# Types of events in probability

# 1. Simple Event:
# Also known as elementary event. A simple event is an event that consists of exactly one outcome.
# Example: When rolling a fair six-sided die, getting a 3 is a simple event.

# 2. Compound Event:
# A compound event consists of two or more simple events.
# Example: When rolling a die, the event "rolling an odd number" is a compound event because it 
# consists of three simple events: rolling a 1, rolling a 3, or rolling a 5.

# 3. Independent Events:
# Two events are independent if the occurrence of one event does not affect the probability of 
# the occurrence of the other event.
# Example: If you flip a coin and roll a die, the outcome of the coin flip does not affect the 
# outcome of the die roll.

# 4. Dependent Events:
# Events are dependent if the occurrence of one event does affect the probability of the 
# occurrence of the other event.
# Example: If you draw two cards from a deck without replacement, the outcome of the first draw 
# affects the outcome of the second draw because there are fewer cards left in the deck.

# 5. Mutually Exclusive Events:
# Two events are mutually exclusive (disjoint) if they cannot both occur at the same time.
# Example: When rolling a die, the events "roll a 2" and "roll a 4" are mutually exclusive 
# because a single roll cannot result in both a 2 and a 4.

# 6. Exhaustive Events:
# A set of events is exhaustive if at least one of the events must occur when the experiment 
# is performed.
# Example: When rolling a die, the events "roll an even number" and "roll an odd number" are 
# exhaustive because one of them must occur on any roll.

# 7. Impossible Event:
# An impossible event is an event that cannot occur at all; its probability is always zero.
# Example: Rolling a 7 on a standard six-sided die is an impossible event.

# 8. Certain Event:
# A certain event is an event that will always occur; its probability is always one.
# Example: When rolling a six-sided die, the event "getting a number between 1 and 6" is a certain event.
